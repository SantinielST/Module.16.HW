namespace Module._16.HW;

public class Calculator
{
    public int Additional(int a, int b) // Метод сложения
    {
        return a + b;
    }

    public int Subtraction(int a, int b) // Метод вычитания
    {
        return a - b;
    }

    public int Miltiplication(int a, int b) // Метод умножения
    {
        return a * b;
    }

    public int Division(int a, int b) // Метод деления
    {
        return a / b;
    }
}
